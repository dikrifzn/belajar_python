#Oprasi Komparasi

#setiap hasil oprasi menghasilkan nilai boolean

a = 5
b = 10

hasil = a > b
print(a, ">", b, "=", hasil)

hasil = a < b
print(a, "<", b, "=", hasil)

hasil = a >= b
print(a, ">=", b, "=", hasil)

hasil = a <= b
print(a, "<=", b, "=", hasil)

hasil = a == b
print(a, "==", b, "=", hasil)

#Tidak Sama Dengan
hasil = a != b
print(a, "!=", b, "=", hasil)

