##Dengan List
angka_list = [1, 2, 3, 4, 5]
for i in angka_list:
    print(i)
print("akhir dari program \n")

##Dengan Range
angka_range = range(5)
for i in angka_range:
    print(i)
print("akhir dari program \n")

angka_range = range(1,5)
for i in angka_range:
    print(i)
print("akhir dari program \n")

##Dengan String
data_str = "Hello World!"
for data in data_str:
    print(data)
print("akhir dari program \n")

