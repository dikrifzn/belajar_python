x = 10
y = 3

hasil = x + y
print(x, "+", y, "=", hasil)

hasil = x - y
print(x, "-", y, "=", hasil)

hasil = x * y
print(x, "*", y, "=", hasil)

hasil = x / y
print(x, "/", y, "=", hasil)

#operasi eksponen
hasil = x ** y
print(x, "**", y, "=", hasil)

#operasi modulus
hasil = x % y
print(x, "%", y, "=", hasil)

#operasi floor devision
hasil = x // y
print(x, "//", y, "=", hasil)

