#Operasi yang dapat dilakukan dengan penyingkatan

#operasi penambahan
a = 5 #adalah assignment
print("nilai a =", a)

a = a + 1 
print("nilai a =", a)

a += 1 #artinya adalah a = a + 1
a -= 1 #artinya adalah a = a - 1
a *= 1 #artinya adalah a = a * 1
a /= 1 #artinya adalah a = a / 1
#modulus
a %= 1 #artinya adalah a = a % 1
#floor devison
a //= 1 #artinya adalah a = a // 1
#Pangkat
a **= 1 #artinya adalah a = a ** 1

#Bitwise
a != True #artinya adalah a tidak True
a |= True #artinya adalah a = a or true
a &= True #artinya adalah a = a and true
a ^= True #artinya adalah a = a xor true

##Shifting

a = 10
#shift right
a >>= 2

#shift left
a <<= 2